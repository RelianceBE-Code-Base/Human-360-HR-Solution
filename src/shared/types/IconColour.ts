/**
 * Represents the allowed colour variants for status or UI icons.
 *
 * - **success** — indicates a positive or completed state
 * - **warning** — indicates caution or something requiring attention
 * - **info** — represents neutral informational messages
 * - **primary** — the default or brand-coloured state
 */
type IconColour = "success" | "warning" | "info" | "primary";
